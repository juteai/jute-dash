func appleMusicSkillActions() []widgetskills.Action {
	actions := make([]widgetskills.Action, 0, len(appleMusicActionCatalog))
	for _, item := range appleMusicActionCatalog {
		actions = append(actions, item.action)
	}
	return actions
}

func invokeAppleMusicAction(
	ctx context.Context,
	client provider.Client,
	actionID string,
	arguments map[string]any,
) (map[string]any, error) {
	for _, item := range appleMusicActionCatalog {
		if item.action.ID == actionID {
			return item.handle(ctx, client, arguments)
		}
	}
	return nil, fmt.Errorf("unknown action: %s", actionID)
}

