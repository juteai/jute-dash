func (w *AppleMusicWidget) FetchDataWithConnections(
	ctx context.Context,
	input widgets.RuntimeInput,
) (widgets.RuntimePayload, error) {
	settings := appleMusicSettingsFromConnection(input.Connections["account"])
	if string(settings.DeveloperToken) == "" {
		return widgets.Unavailable(
			"connection.missing_credentials",
			"Apple Music account needed",
			"Choose an Apple Music Account connection in settings.",
		), nil
	}
	if string(settings.UserToken) == "" {
		return widgets.OK(map[string]any{
			"track_title":      "Not Playing",
			"artist_name":      "Unknown",
			"album_art_url":    "",
			"is_playing":       false,
			"volume":           50,
			"progress_ms":      0,
			"duration_ms":      0,
			"shuffle":          false,
			"repeat_state":     "off",
			"top_albums":       []map[string]any{},
			"needs_user_token": true,
		}), nil
	}
	client := provider.NewClient(providerSettings(settings))
	playback, err := client.FetchPlayback(ctx)
	if err != nil {
		return widgets.Unavailable( //nolint:nilerr // provider error is mapped to a safe widget issue
			"apple_music.unavailable",
			"Apple Music unavailable",
			"Jute could not load Apple Music playback.",
		), nil
	}
	if len(playback.TopAlbums) == 0 {
		playback.TopAlbums = appleMusicSuggestions.get(
			ctx,
			appleMusicSuggestionCacheKey(input),
			client.FetchSuggestedAlbums,
		)
	}
	return widgets.OK(appleMusicPlaybackData(playback)), nil
}

