func appleMusicPlaybackCatalogItem(id, title, description string) appleMusicActionCatalogItem {
	return appleMusicActionCatalogItem{
		action: appleMusicPlaybackAction(id, title, description),
		handle: func(ctx context.Context, client provider.Client, arguments map[string]any) (map[string]any, error) {
			if err := client.ApplyAction(ctx, id, arguments); err != nil {
				return nil, err
			}
			return map[string]any{"status": "ok"}, nil
		},
	}
}

func appleMusicPlaybackAction(id, title, description string) widgetskills.Action {
	properties := map[string]any{}
	switch id {
	case "set_volume":
		properties["volume"] = map[string]any{"type": "number", "minimum": 0, "maximum": 100}
	case "set_shuffle":
		properties["state"] = map[string]any{"type": "boolean"}
	case "set_repeat":
		properties["state"] = map[string]any{"type": "string", "enum": []string{"off", "context", "track"}}
	case "seek":
		properties["position_ms"] = map[string]any{"type": "number", "minimum": 0}
	case "play", "play_album", "play_track", "play_playlist":
		properties["uri"] = map[string]any{"type": "string"}
		properties["query"] = map[string]any{"type": "string"}
	}
	return widgetskills.Action{
		ID:                   id,
		Title:                title,
		Description:          description,
		SideEffect:           "home_action",
		RequiresConfirmation: false,
		InputSchema: map[string]any{
			"type":                 "object",
			"properties":           properties,
			"additionalProperties": true,
		},
		OutputSchema: map[string]any{
			"type":       "object",
			"properties": map[string]any{"status": map[string]any{"type": "string"}},
			"required":   []string{"status"},
		},
	}
}

func stringArgument(arguments map[string]any, key string) string {
	value, _ := arguments[key].(string)
	return strings.TrimSpace(value)
}

func intArgument(arguments map[string]any, key string, fallback int) int {
	switch value := arguments[key].(type) {
	case int:
		if value > 0 {
			return value
		}
	case float64:
		if value > 0 {
			return int(value)
		}
	}
	return fallback
}

func appleMusicSuggestionCacheKey(input widgets.RuntimeInput) string {
	if connection := input.Connections["account"]; connection.ID != "" {
		return connection.ID
	}
	if ref := input.ConnectionRefs["account"]; ref != "" {
		return ref
	}
	return input.InstanceID + ":account"
}

type appleMusicSuggestionFetcher func(context.Context, int) ([]provider.PlayableItem, error)

type appleMusicSuggestionCache struct {
	mu      sync.Mutex
	entries map[string]appleMusicSuggestionCacheEntry
	now     func() time.Time
}

type appleMusicSuggestionCacheEntry struct {
	items      []provider.PlayableItem
	expiresAt  time.Time
	retryAfter time.Time
}

func newAppleMusicSuggestionCache() *appleMusicSuggestionCache {
	return &appleMusicSuggestionCache{
		entries: map[string]appleMusicSuggestionCacheEntry{},
		now:     time.Now,
	}
}

func (c *appleMusicSuggestionCache) get(
	ctx context.Context,
	key string,
	fetch appleMusicSuggestionFetcher,
) []provider.PlayableItem {
	now := c.now()
	c.mu.Lock()
	entry := c.entries[key]
	if len(entry.items) > 0 && now.Before(entry.expiresAt) {
		items := cloneAppleMusicItems(entry.items)
		c.mu.Unlock()
		return items
	}
	if now.Before(entry.retryAfter) {
		items := cloneAppleMusicItems(entry.items)
		c.mu.Unlock()
		return items
	}
	c.mu.Unlock()

	items, err := fetch(ctx, 8)

	c.mu.Lock()
	defer c.mu.Unlock()
	if err != nil {
		entry.retryAfter = now.Add(5 * time.Minute)
		c.entries[key] = entry
		return cloneAppleMusicItems(entry.items)
	}
	entry.items = cloneAppleMusicItems(items)
	if len(items) > 0 {
		entry.expiresAt = now.Add(30 * time.Minute)
	} else {
		entry.expiresAt = now.Add(5 * time.Minute)
	}
	entry.retryAfter = time.Time{}
	c.entries[key] = entry
	return cloneAppleMusicItems(entry.items)
}

func cloneAppleMusicItems(items []provider.PlayableItem) []provider.PlayableItem {
	if len(items) == 0 {
		return nil
	}
	out := make([]provider.PlayableItem, len(items))
	copy(out, items)
	return out
}
